    private void loadData(){
        model = new DefaultTableModel();
        model = new DefaultTableModel();
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();

        model.addColumn("NoFaktur");
        model.addColumn("KodeBarang");
        model.addColumn("NamaBarang");
        model.addColumn("HargaJual");
        model.addColumn("Stok");
        model.addColumn("Jumlah");
        model.addColumn("SubTotal");
        model.addColumn("IDPetugas");
        model.addColumn("TanggalPenjualan");
        model.addColumn("NamaPetugas");
        model.addColumn("Bayar");
        model.addColumn("Sisa");
        model.addColumn("Total");
        
        try {
            String sql = "SELECT A.NoFaktur, A.TglPenjualan, A.IDPetugas, A.Bayar, A.Sisa, A.Total, B.NoFaktur, B.KodeBarang, B.Jumlah, B.SubTotal, C.KodeBarang, C.NamaBarang, C.HargaJual, C.Stok, D.NamaPetugas "
                    + "FROM tbldetailpenjualan B "
                    + "LEFT JOIN tblpenjualan A ON B.NoFaktur = A.NoFaktur "
                    + "LEFT JOIN tblbarang C ON B.KodeBarang = C.KodeBarang "
                    + "LEFT JOIN tblpetugas D ON A.IDPetugas = D.IDPetugas";
            
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            ResultSet r = s.executeQuery(sql);
            while (r.next()) {
                model.addRow(new Object[]{
                    r.getString("NoFaktur"),
                    r.getString("KodeBarang"),
                    r.getString("NamaBarang"),
                    r.getString("HargaJual"),
                    r.getString("Stok"),
                    r.getString("Jumlah"),
                    r.getString("SubTotal"),
                    r.getString("IDPetugas"),
                    r.getDate("TglPenjualan"),
                    r.getString("NamaPetugas"),
                    r.getString("Bayar"),
                    r.getString("Sisa"),
                    r.getString("Total")
                });
            }
            
            tblpenjualan.setModel(model);
        } catch (SQLException e) {
            e.printStackTrace();
            System.out.println("Terjadi Error");
        }   
}
    
