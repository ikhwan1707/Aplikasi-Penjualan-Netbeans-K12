    private void getData() {

        String NM = txtfaktur.getText();
        String KB = cmbkodebarang.getSelectedItem().toString();
        String JM = txtjumlah.getText();

        if ((NM.isEmpty()) | (KB.isEmpty()) | (JM.isEmpty())) {
            JOptionPane.showMessageDialog(null, "data tidak boleh kosong, silahkan dilengkapi");
            cmbkodebarang.requestFocus();
        } else {
            try {
                Connection c = Koneksi.getKoneksi();
                Statement stt = c.createStatement();
                String SQL = "insert into tbldetailpenjualan values('" + txtfaktur.getText() + "',"
                        + "'" + cmbkodebarang.getSelectedItem() + "',"
                        + "'" + txtjumlah.getText() + "',"
                        + "'" + txtsubtotal.getText() + "')";
                stt.executeUpdate(SQL);

                Connection kon1 = Koneksi.getKoneksi();
                Statement stt1 = kon1.createStatement();
                String SQL1 = "Update tblbarang Set stok=stok - '" + txtjumlah.getText() + "'"
                        + "Where kodebarang='" + cmbkodebarang.getSelectedItem().toString() + "'";
                stt1.executeUpdate(SQL1);
                String[] data = new String[7];
                data[0] = txtfaktur.getText();
                data[1] = cmbkodebarang.getSelectedItem().toString();
                data[2] = txtnmbarang.getText();
                data[3] = txthrgjual.getText();
                data[4] = txtstok.getText();
                data[5] = txtjumlah.getText();
                data[6] = txtsubtotal.getText();
                model.insertRow(0, data);

                cmbkodebarang.requestFocus();
                btnadd.setEnabled(false);
                btnsave.setEnabled(true);
                cmbkodebarang.requestFocus();
            } catch (Exception ex) {
                System.out.println("Terjadi Error" + ex.getMessage());
            } finally {
                kosong();
            }
        }
    }
    
