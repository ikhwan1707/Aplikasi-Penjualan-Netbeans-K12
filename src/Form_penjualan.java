    private void tblpenjualanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblpenjualanMouseClicked
        // TODO add your handling code here:
        int baris = tblpenjualan.getSelectedRow();
        if (baris == -1) {
            return;
        }

        if (tblpenjualan.getColumnCount() >= 13) {
            String kj = String.valueOf(tblpenjualan.getValueAt(baris, 0));
            txtfaktur.setText(kj);
            cmbkodebarang.setSelectedItem(tblpenjualan.getValueAt(baris, 1));
            String NamaBarang = String.valueOf(tblpenjualan.getValueAt(baris, 2));
            txtnmbarang.setText(NamaBarang);
            String HargaJual = String.valueOf(tblpenjualan.getValueAt(baris, 3));
            txthrgjual.setText(HargaJual);
            String Stok = String.valueOf(tblpenjualan.getValueAt(baris, 4));
            txtstok.setText(Stok);
            String jumlah = String.valueOf(tblpenjualan.getValueAt(baris, 5));
            txtjumlah.setText(jumlah);
            String SubTotal = String.valueOf(tblpenjualan.getValueAt(baris, 6));
            txtsubtotal.setText(SubTotal);
        }

        if (baris >= 0) {

            if (tblpenjualan.getValueAt(baris, 7) != null) {
                cmbpetugas.setSelectedItem(tblpenjualan.getValueAt(baris, 7));
            }

            java.util.Date TanggalPenjualan = (java.util.Date) tblpenjualan.getValueAt(baris, 8);
            txttanggal.setValue(TanggalPenjualan);
            String NamaPetugas = String.valueOf(tblpenjualan.getValueAt(baris, 9));
            txtpetugas.setText(NamaPetugas);
            String Bayar = String.valueOf(tblpenjualan.getValueAt(baris, 10));
            txtbayar.setText(Bayar);
            String sisa = String.valueOf(tblpenjualan.getValueAt(baris, 11));
            txtsisa.setText(sisa);
            String total = String.valueOf(tblpenjualan.getValueAt(baris, 12));
            txttotal.setText(total);

        }
    }//GEN-LAST:event_tblpenjualanMouseClicked

